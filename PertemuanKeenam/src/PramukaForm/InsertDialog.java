    private void bSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSaveActionPerformed
        // TODO add your handling code here:
        try {
            String NTA = tfNTA.getText();
            String Nama = tfNama.getText();
            String Almt = tfAlamat.getText();
            String jk = cbJk.getSelectedItem().toString();

            if (NTA.length() != 5) {
                JOptionPane.showMessageDialog(this,
                        "NTA harus terdiri dari tepat 5 karakter!",
                        "Input Error",
                        JOptionPane.ERROR_MESSAGE);
                return; // hentikan eksekusi simpan
            }

            Connection conn = DriverManager.getConnection(
                    "jdbc:postgresql://localhost:5432/kuliahPBO",
                    "postgres",
                    "kapuas02"
            );
            String sql = "INSERT INTO Pramuka (NTA, Nama, Alamat, JenisKelamin) VALUES (?, ?, ?, ?)";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, NTA);
            pstmt.setString(2, Nama);
            pstmt.setString(3, Almt);
            pstmt.setString(4, jk);
            pstmt.executeUpdate();

            saved = true; // tandai berhasil
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
            conn.close();
            hilang();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }

    }//GEN-LAST:event_bSaveActionPerformed

